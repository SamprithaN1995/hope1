
public class Encapsulate {
	
	int age;
	String name;
	double salary;
	
	public void setage(int age)
	{
		this.age=age;
	}
	public int getage()
	{
		return age;
	}
	
	public void setname(String name)
	{
		this.name = name;
	}

	public String getname()
	{
		return name;
	}
	
	public void setsalary(double salary)
	{
		this.salary=salary;
	}
	
	public double getsalary()
	{
		return salary;
	}
}
